abstract class FullScreenStatus {
  const FullScreenStatus();
}

class FullScreenStatusInactive extends FullScreenStatus {
  const FullScreenStatusInactive();
}

class FullScreenStatusOpened extends FullScreenStatus {
  const FullScreenStatusOpened();
}

class FullScreenStatusHidden extends FullScreenStatus {
  const FullScreenStatusHidden();
}

class FullScreenStatusClosing extends FullScreenStatus {
  const FullScreenStatusClosing();
}

class FullScreenStatusClosed extends FullScreenStatus {
  const FullScreenStatusClosed();
}
